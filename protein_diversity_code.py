#take in a string of letters for a given site
#return Shannon information for that site

def shannon_info(site, AAs=AAs):

    temp_info = 0

    for aa in AAs:

        if aa in site:

            temp_info += site.count(aa)/len(site) * math.log2(site.count(aa)/len(site))

    return abs(temp_info)


